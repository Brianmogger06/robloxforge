export type BtNodeKind = "sequence" | "selector" | "action" | "condition" | "repeat";

export interface BtNode {
  kind: BtNodeKind;
  name: string;
  action?: string;
  condition?: string;
  args?: Record<string, unknown>;
  children?: BtNode[];
  repeatCount?: number;
}

export interface BehaviorTree {
  npcId: string;
  name: string;
  root: BtNode;
}

// ─── Presets ──────────────────────────────────────────────────────────────────

export function patrolBehavior(waypoints: Array<{ x: number; y: number; z: number }>): BehaviorTree {
  return {
    npcId: "", name: "Patrol",
    root: {
      kind: "repeat", name: "PatrolLoop", repeatCount: -1,
      children: [
        {
          kind: "sequence", name: "PatrolSequence",
          children: waypoints.map((wp, i) => ({
            kind: "action" as const, name: `MoveTo_${i}`,
            action: "moveTo",
            args: { position: wp },
          })),
        },
      ],
    },
  };
}

