// ─── Validation ───────────────────────────────────────────────────────────────

export function validateBehaviorTree(bt: BehaviorTree): { valid: boolean; errors: string[] } {
  const errors: string[] = [];
  function walk(node: BtNode, path: string) {
    if (!node.kind) errors.push(`${path}: missing 'kind'`);
    if (!node.name) errors.push(`${path}: missing 'name'`);
    if (node.kind === "action"    && !node.action)    errors.push(`${path}: action node missing 'action'`);
    if (node.kind === "condition" && !node.condition) errors.push(`${path}: condition node missing 'condition'`);
    if (node.children) {
      for (const child of node.children) walk(child, `${path}.${child.name ?? "?"}`);
    }
  }
  walk(bt.root, "root");
  return { valid: errors.length === 0, errors };
}

// ─── ModuleScript source generator ───────────────────────────────────────────

export function generateBehaviorScript(bt: BehaviorTree): string {
  const json = JSON.stringify(bt, null, 2).replace(/"/g, '\\"');
  return `--!strict
-- Generated by RobloxForge NPC Intelligence
-- BehaviorTree: ${bt.name}
local PathfindingService = game:GetService("PathfindingService")
local BT_DATA = game:GetService("HttpService"):JSONDecode([==[${JSON.stringify(bt)}]==])

local M = {}

function M.init(npc: Model)
	-- Simple BT interpreter
	local humanoid = npc:FindFirstChildOfClass("Humanoid")
	if not humanoid then return end
	-- Patrol: moveTo each waypoint in sequence
	local root = BT_DATA.root
	if root.kind == "repeat" then
		task.spawn(function()
			while true do
				for _, seq in ipairs(root.children or {}) do
					for _, actionNode in ipairs(seq.children or {}) do
						if actionNode.action == "moveTo" and actionNode.args then
							local pos = actionNode.args.position
							if pos then
								local path = PathfindingService:CreatePath({AgentRadius=2,AgentHeight=5})
								local ok = pcall(function()
									path:ComputeAsync(npc:GetPivot().Position, Vector3.new(pos.x,pos.y,pos.z))
								end)
								if ok and path.Status == Enum.PathStatus.Success then
									for _, wp in ipairs(path:GetWaypoints()) do
										humanoid:MoveTo(wp.Position)
										humanoid.MoveToFinished:Wait()
									end
								end
							end
						end
					end
				end
			end
		end)
	end
end

return M
`;
}
