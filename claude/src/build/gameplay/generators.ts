export interface GameplayModule {
  name: string;
  source: string;
  description: string;
}

// All modules export { init(player), set(player, k, v), get(player, k) }
// so Phase 10 validation can swap them uniformly.

export function generateCurrencySystem(options: {
  currencyName?: string;
  startAmount?: number;
  dataStoreKey?: string;
} = {}): GameplayModule {
  const name      = options.currencyName  ?? "Coins";
  const start     = options.startAmount   ?? 0;
  const dsKey     = options.dataStoreKey  ?? "CurrencyData_v1";

  return {
    name: `${name}System`,
    description: `DataStore-backed ${name} currency with Get/Add/Spend API`,
    source: `--!strict
-- ${name} System — generated by RobloxForge
local DataStoreService = game:GetService("DataStoreService")
local Players          = game:GetService("Players")
local ReplicatedStorage = game:GetService("ReplicatedStorage")

local DS = DataStoreService:GetDataStore("${dsKey}")
local cache: {[string]: number} = {}

-- RemoteEvents
local rf = Instance.new("RemoteFunction", ReplicatedStorage)
rf.Name = "Get${name}"
local addRE = Instance.new("RemoteEvent", ReplicatedStorage)
addRE.Name = "Update${name}"

local M = {}

function M.get(player: Player): number
	return cache[player.UserId] or ${start}
end

function M.set(player: Player, _k: string, v: number)
	cache[player.UserId] = v
	addRE:FireClient(player, v)
end

function M.add(player: Player, amount: number): number
	local current = M.get(player)
	local newVal = math.max(0, current + amount)
	M.set(player, "amount", newVal)
	return newVal
end

function M.spend(player: Player, amount: number): boolean
	if M.get(player) < amount then return false end
	M.add(player, -amount)
	return true
end

function M.init(player: Player)
	local ok, data = pcall(function() return DS:GetAsync(tostring(player.UserId)) end)
	cache[player.UserId] = (ok and type(data) == "number" and data) or ${start}
	rf.OnServerInvoke = function(p) return M.get(p) end
end

Players.PlayerRemoving:Connect(function(player)
	pcall(function() DS:SetAsync(tostring(player.UserId), M.get(player)) end)
	cache[player.UserId] = nil
end)

return M
`,
  };
}

export function generateUpgradeSystem(options: {
  upgrades?: Array<{ id: string; name: string; cost: number; maxLevel: number }>;
  currencyName?: string;
} = {}): GameplayModule {
  const currency = options.currencyName ?? "Coins";
  const upgrades = options.upgrades ?? [
    { id: "speed",    name: "Speed",    cost: 100, maxLevel: 5 },
    { id: "capacity", name: "Capacity", cost: 150, maxLevel: 3 },
  ];
  const upgradeJson = JSON.stringify(upgrades, null, 2);

  return {
    name: "UpgradeSystem",
    description: "Server-validated upgrade tiers with DataStore persistence",
    source: `--!strict
-- Upgrade System — generated by RobloxForge
local DataStoreService  = game:GetService("DataStoreService")
local ReplicatedStorage  = game:GetService("ReplicatedStorage")
local DS = DataStoreService:GetDataStore("UpgradeData_v1")

local UPGRADES = game:GetService("HttpService"):JSONDecode([==[${upgradeJson}]==])
local cache: {[string]: {[string]: number}} = {}

local re = Instance.new("RemoteEvent", ReplicatedStorage)
re.Name = "BuyUpgrade"
local rf = Instance.new("RemoteFunction", ReplicatedStorage)
rf.Name = "GetUpgrades"

local M = {}

function M.get(player: Player, k: string): number
	return (cache[player.UserId] or {})[k] or 0
end

function M.set(player: Player, k: string, v: number)
	cache[player.UserId] = cache[player.UserId] or {}
	cache[player.UserId][k] = v
end

function M.init(player: Player)
	local ok, data = pcall(function() return DS:GetAsync(tostring(player.UserId)) end)
	cache[player.UserId] = (ok and type(data) == "table" and data) or {}
	rf.OnServerInvoke = function(p) return cache[p.UserId] or {} end
end

re.OnServerEvent:Connect(function(player, upgradeId)
	for _, up in ipairs(UPGRADES) do
		if up.id == upgradeId then
			local level = M.get(player, upgradeId)
			if level >= up.maxLevel then return end
			local CoinSystem = require(script.Parent:FindFirstChild("${currency}System"))
			if CoinSystem.spend(player, up.cost * (level + 1)) then
				M.set(player, upgradeId, level + 1)
			end
			return
		end
	end
end)

return M
`,
  };
}

export function generateQuestSystem(options: {
  quests?: Array<{ id: string; name: string; description: string; goal: number }>;
} = {}): GameplayModule {
  const quests = options.quests ?? [
    { id: "collect_10", name: "Collector",    description: "Collect 10 items", goal: 10 },
    { id: "earn_100",   name: "First Earner", description: "Earn 100 coins",   goal: 100 },
  ];
  const questJson = JSON.stringify(quests, null, 2);

  return {
    name: "QuestSystem",
    description: "Quest registry with per-player progress in DataStore",
    source: `--!strict
-- Quest System — generated by RobloxForge
local DataStoreService  = game:GetService("DataStoreService")
local ReplicatedStorage  = game:GetService("ReplicatedStorage")
local DS = DataStoreService:GetDataStore("QuestData_v1")
local QUESTS = game:GetService("HttpService"):JSONDecode([==[${questJson}]==])
local progress: {[string]: {[string]: number}} = {}

local rf = Instance.new("RemoteFunction", ReplicatedStorage)
rf.Name = "GetQuests"
local re = Instance.new("RemoteEvent", ReplicatedStorage)
re.Name = "QuestProgress"

local M = {}

function M.get(player: Player, k: string): number
	return (progress[player.UserId] or {})[k] or 0
end

function M.set(player: Player, questId: string, value: number)
	progress[player.UserId] = progress[player.UserId] or {}
	progress[player.UserId][questId] = value
	re:FireClient(player, questId, value)
end

function M.advance(player: Player, questId: string, amount: number)
	M.set(player, questId, M.get(player, questId) + amount)
end

function M.init(player: Player)
	local ok, data = pcall(function() return DS:GetAsync(tostring(player.UserId)) end)
	progress[player.UserId] = (ok and type(data) == "table" and data) or {}
	rf.OnServerInvoke = function(p)
		local result = {}
		for _, q in ipairs(QUESTS) do
			result[q.id] = { quest = q, current = M.get(p, q.id), complete = M.get(p, q.id) >= q.goal }
		end
		return result
	end
end

return M
`,
  };
}

export function generateRoundSystem(options: {
  intermissionDuration?: number;
  roundDuration?: number;
  minPlayers?: number;
} = {}): GameplayModule {
  const intermission = options.intermissionDuration ?? 15;
  const roundTime    = options.roundDuration        ?? 180;
  const minPlayers   = options.minPlayers           ?? 2;

  return {
    name: "RoundSystem",
    description: "Intermission → InRound → Postgame loop with RemoteEvents",
    source: `--!strict
-- Round System — generated by RobloxForge
local Players           = game:GetService("Players")
local ReplicatedStorage  = game:GetService("ReplicatedStorage")

local stateRE = Instance.new("RemoteEvent", ReplicatedStorage)
stateRE.Name = "RoundState"

local INTERMISSION = ${intermission}
local ROUND_TIME   = ${roundTime}
local MIN_PLAYERS  = ${minPlayers}

local M = {}
local currentState = "Intermission"

function M.get(_player: Player, k: string): string
	if k == "state" then return currentState end
	return ""
end

function M.set(_player: Player, k: string, v: string)
	if k == "state" then currentState = v end
end

function M.init(_player: Player) end

local function broadcast(state: string, data: {[string]: unknown}?)
	currentState = state
	stateRE:FireAllClients(state, data or {})
end

task.spawn(function()
	while true do
		broadcast("Intermission", {duration = INTERMISSION})
		task.wait(INTERMISSION)
		if #Players:GetPlayers() < MIN_PLAYERS then continue end
		broadcast("InRound", {duration = ROUND_TIME})
		task.wait(ROUND_TIME)
		broadcast("Postgame", {})
		task.wait(5)
	end
end)

return M
`,
  };
}

